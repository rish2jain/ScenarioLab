    def _generate_monte_carlo(self, report: SimulationReport) -> str:
        """Generate Monte Carlo visualization section."""
        # Check if Monte Carlo data is available in results_summary
        mc_data = None
        if hasattr(report, "results_summary") and report.results_summary:
            mc_data = report.results_summary.get("monte_carlo")

        if not mc_data:
            # Generate placeholder bars
            bars_html = ""
            outcomes = [
                ("Best Case", 15),
                ("Expected", 45),
                ("Downside", 30),
                ("Worst Case", 10),
            ]
