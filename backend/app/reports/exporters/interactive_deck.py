"""Interactive presentation deck exporter for simulation reports.

Generates a self-contained HTML file with embedded JavaScript for
interactive presentation of simulation results.
"""

import logging
from datetime import datetime

from app.reports.models import SimulationReport
from app.simulation.models import SimulationState

logger = logging.getLogger(__name__)


class InteractiveDeckExporter:
    """Exports simulation reports as interactive HTML presentation decks."""

    def __init__(
        self,
        logo_url: str | None = None,
        primary_color: str = "#3B82F6",
        company_name: str = "ScenarioLab",
    ):
        self.logo_url = logo_url
        self.primary_color = primary_color
        self.company_name = company_name

    def export_interactive_deck(
        self,
        report: SimulationReport,
        simulation_state: SimulationState | None = None,
    ) -> str:
        """Generate a self-contained interactive HTML presentation.

        Args:
            report: The simulation report to export
            simulation_state: Optional simulation state for additional context

        Returns:
            Complete HTML string with embedded CSS and JavaScript
        """
        html = self._generate_html(report, simulation_state)
        return html

    def _generate_html(
        self,
        report: SimulationReport,
        simulation_state: SimulationState | None,
    ) -> str:
        """Generate the complete HTML document."""
        return f"""<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>{self._escape_html(report.simulation_name)} - Report</title>
    <style>
{self._generate_css()}
    </style>
</head>
<body>
    <div class="theme-toggle">
        <button onclick="toggleTheme()" id="themeBtn">
            Toggle Dark/Light
        </button>
    </div>
    <div class="container">
{self._generate_cover_section(report)}
{self._generate_agent_personas(report, simulation_state)}
{self._generate_risk_register(report)}
{self._generate_scenario_matrix(report)}
{self._generate_stakeholder_heatmap(report)}
{self._generate_key_findings(report)}
{self._generate_recommendations(report)}
{self._generate_monte_carlo(report)}
    </div>
    <footer>
        Generated by {self._escape_html(self.company_name)} on {
            datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")
        }
    </footer>
    <script>
{self._generate_javascript()}
    </script>
</body>
</html>"""

